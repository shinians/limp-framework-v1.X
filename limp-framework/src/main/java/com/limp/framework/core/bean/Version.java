package com.limp.framework.core.bean;

import com.limp.framework.boss.filter.PageLogUtils;

/**
 * @author:shinian
 * @description:
 * @date:Created in 14:54 2017/10/20
 * @modified By:
 */
public final  class Version {
    /**
     * Private constructor for CasVersion. You should not be able to instantiate
     * this class.
     */
    private Version(){
        //此方法不允许实例化
    }

    /**
     * @return Return the full limp version string.
     * @see java.lang.Package#getImplementationVersion
     */
    public static String getVersion(){
        PageLogUtils.format("LP V: %s{0}\n", Version.class.getPackage().getImplementationVersion());
        return Version.class.getPackage().getImplementationVersion();
    }

}
